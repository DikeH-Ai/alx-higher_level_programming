#!/usr/bin/python3

"""
Function fthat returns the addition of two integers
"""


def add(a, b):
    """
    Function to add two numbers.

    :param a: The first number.
    :param b: The second number.
    :return: The sum of a and b.
    """
    return a + b

if __name__ == "__main__":
    add(a, b)
